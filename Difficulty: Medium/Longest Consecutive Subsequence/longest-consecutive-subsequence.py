class Solution:
    
    # arr[] : the input array
    
    #Function to return length of longest subsequence of consecutive integers.
    def longestConsecutive(self,arr):
        #code here
        arr = sorted(set(arr))  
        n = arr[0]
        count = 0
        inc = 1
        
        for i in range(1, len(arr)):
            n += 1
            if n == arr[i]:
                inc += 1
            else:
                n = arr[i]
                count = max(count, inc)
                inc = 1
        return max(inc, count)

